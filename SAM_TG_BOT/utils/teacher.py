import sqlite3

def get_all_teacher_telegram_ids(db_path='data/base.db'):
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()

    cursor.execute("SELECT telegram_id FROM teachers WHERE telegram_id IS NOT NULL")
    telegram_ids = [row[0] for row in cursor.fetchall()]

    conn.close()
    return telegram_ids

